class Util:
    @staticmethod
    def inchesToMeters(inches):
        return inches * 0.0254

    @staticmethod
    def metersToInches(meters):
        return meters / 0.0254

    @staticmethod
    def millimetersToMeters(mm):
        return mm / 1000

    @staticmethod
    def metersToMillimeterss(m):
        return 1000 * m
